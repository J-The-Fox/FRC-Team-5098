package frc.components;

/**
 * Interface for components.
 */
public interface IComponent {

    // This is an interface.
    // If a class implements any interface,
    // it must have all of the methods in that interface.
    // The methods defined in an interface are abstract,
    // meaning they have no body.

    /**
     * Method for updating the component.
     */
    void update();
}
